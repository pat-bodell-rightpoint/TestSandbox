package com.example.codetestingexample

class MathUtil {
    fun add(leftVal: Int, rightVal: Int) = leftVal + rightVal
    fun subtract(leftVal: Int, rightVal: Int) = leftVal - rightVal
    fun multiply(leftVal: Int, rightVal: Int) = leftVal * rightVal
    fun divide(leftVal: Int, rightVal: Int) = leftVal / rightVal
}
